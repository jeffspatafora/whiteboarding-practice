def fizzbuzz(numbers_array)
  new_numbers_array = []
  i = 0
  while i < numbers_array.length
    if numbers_array[i] % 3 == 0 && numbers_array[i] % 5 == 0
      new_numbers_array << "fizzbuzz"
    elsif numbers_array[i] % 3 == 0
      new_numbers_array << "fizz"
    elsif numbers_array[i] % 5 == 0
      new_numbers_array << "buzz"
    else
      new_numbers_array << numbers_array[i]
    end
    i += 1
  end
  new_numbers_array
end

